void fan::Init(void)
{
   arduino::Init_PWM( FAN_CTL_PWM_CHANNEL );
}

void fan::Set_Output(const uint8_t out)
{
   if(out < AXIAL_FAN_MAX_OUTPUT &&
      out > AXIAL_FAN_MIN_OUTPUT)
   {
      Output = out;
   }
   else if (out >= AXIAL_FAN_MAX_OUTPUT)
   {
      Output = AXIAL_FAN_MAX_OUTPUT;
   }
   else
   {
      Output = AXIAL_FAN_MIN_OUTPUT;
   }
   arduino::Set_PWM( FAN_CTL_PWM_CHANNEL, out);
}

uint16_t fan::Get_Output(void)
{
   return Output;
}

void fan::Shut(void)
{
   arduino::Stop_PWM( FAN_CTL_PWM_CHANNEL );
}
