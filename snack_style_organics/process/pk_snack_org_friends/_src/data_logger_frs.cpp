void logger::DATA_LOGGER_STREAM_UART_print_int(const int d)
{
   memset(data_stream, 0x00, sizeof(data_stream));
   char * ss = data_stream;
   sprintf(data_stream,"%d", d);

   for (; ss != 0; ++ss)
   {
      arduino::Put_UART(ARDUINO_UART_CHANNEL_0, ss);
   }
}

