const uint8_t ADC_Channel_To_Pin [] =
{
   9,/*ARDUINO_ADC_CHANNEL_0*/
   5,/*ARDUINO_ADC_CHANNEL_1*/
   4,/*ARDUINO_ADC_CHANNEL_2*/
};
